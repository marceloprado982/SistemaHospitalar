 create table internacao_entity (
       internacao_paciente int generated by default as identity,
        data_hora_entrada varchar(255),
        data_hora_saida varchar(255),
        diagnostico varchar(255),
        nome_paciente varchar(255),
        paciente_id int,
        medico_id int,
        primary key (internacao_paciente),
        constraint fk_id_paciente
        	foreign key(paciente_id)
        	references paciente(id),
        	constraint fk_nome_medico
        	foreign key(medico)
        	references profissional_saude(id)
    );

  create table paciente_entity (
       id int generated by default as identity,
        data_nascimento varchar(255),
        nome varchar(255),
        telefone varchar(255),
        primary key (id)
    );


 create table profissional_saude_entity (
       id int generated by default as identity,
        cargo varchar(255),
        departamento varchar(255),
        matricula int4 not null,
        nome varchar(255),
        telefone_profissional varchar(255),
        primary key (id)
    );

